-- WS_CBTA: one row per (series_key, time_period) observation.
-- Raw already carries typed obs_value / period_start and split code+label
-- dimension columns, so this is a projection plus the missing-observation gate.
SELECT
    "series_key",
    "freq",
    "freq_label",
    "ref_area",
    "ref_area_label",
    "comp_method",
    "comp_method_label",
    "unit_measure",
    "unit_measure_label",
    "currency",
    "currency_label",
    "transformation",
    "transformation_label",
    "time_period",
    "period_start",
    "obs_value",
    "comment_dset",
    "data_comp",
    "method_ref",
    "collection_detail",
    "comment_ts",
    "decimals",
    "unit_mult",
    "breaks",
    "supp_info_breaks",
    "compiling_org",
    "diss_org",
    "title",
    "time_format",
    "collection",
    "fiscal_year",
    "conf_status",
    "obs_status",
    "obs_pre_break"
FROM "bis-ws-cbta"
WHERE obs_value IS NOT NULL
