"""Download nodes for INEC Ecuador's public ANDA/NADA catalog metadata.

The public API exposes study-level metadata and DDI/codebook information without
authentication. It does not expose direct microdata file URLs, so each accepted
catalog entity is fetched as a one-row SQL-readable metadata table while the full
upstream JSON document is preserved in ``metadata_json`` for downstream modeling.
"""
from __future__ import annotations

import json
from urllib.parse import quote

import pyarrow as pa

from constants import ENTITY_IDNO
from subsets_utils import NodeSpec, get, save_raw_parquet

BASE_URL = "https://anda.inec.gob.ec/anda5/index.php/api/catalog"
PREFIX = "inec-ecuador-"

SCHEMA = pa.schema([
    ("source_entity_id", pa.string()),
    ("id", pa.string()),
    ("idno", pa.string()),
    ("doi", pa.string()),
    ("repositoryid", pa.string()),
    ("type", pa.string()),
    ("title", pa.string()),
    ("subtitle", pa.string()),
    ("year_start", pa.int32()),
    ("year_end", pa.int32()),
    ("nation", pa.string()),
    ("authoring_entity", pa.string()),
    ("published", pa.string()),
    ("created", pa.string()),
    ("changed", pa.string()),
    ("varcount", pa.int32()),
    ("total_views", pa.int64()),
    ("total_downloads", pa.int64()),
    ("data_access_type", pa.string()),
    ("remote_data_url", pa.string()),
    ("metadata_json", pa.string()),
])


def _to_int(value):
    if value is None or value == "":
        return None
    return int(str(value).strip())


def _entity_from_node_id(node_id: str) -> str:
    if not node_id.startswith(PREFIX):
        raise ValueError(f"unexpected node id: {node_id}")
    return node_id[len(PREFIX):]


def fetch_study_metadata(node_id: str) -> None:
    entity_id = _entity_from_node_id(node_id)
    idno = ENTITY_IDNO[entity_id]
    url = f"{BASE_URL}/{quote(idno, safe='')}"
    resp = get(url, params={"format": "json"}, timeout=(10.0, 180.0))
    resp.raise_for_status()
    payload = resp.json()
    if payload.get("status") != "success" or not isinstance(payload.get("dataset"), dict):
        raise RuntimeError(f"{node_id}: unexpected API response for {idno}: {payload!r}")

    dataset = payload["dataset"]
    row = {
        "source_entity_id": entity_id,
        "id": str(dataset.get("id") or entity_id),
        "idno": dataset.get("idno") or idno,
        "doi": dataset.get("doi"),
        "repositoryid": dataset.get("repositoryid"),
        "type": dataset.get("type"),
        "title": dataset.get("title"),
        "subtitle": dataset.get("subtitle"),
        "year_start": _to_int(dataset.get("year_start")),
        "year_end": _to_int(dataset.get("year_end")),
        "nation": dataset.get("nation"),
        "authoring_entity": dataset.get("authoring_entity"),
        "published": dataset.get("published"),
        "created": dataset.get("created"),
        "changed": dataset.get("changed"),
        "varcount": _to_int(dataset.get("varcount")),
        "total_views": _to_int(dataset.get("total_views")),
        "total_downloads": _to_int(dataset.get("total_downloads")),
        "data_access_type": dataset.get("data_access_type"),
        "remote_data_url": dataset.get("remote_data_url"),
        "metadata_json": json.dumps(dataset, ensure_ascii=False, sort_keys=True),
    }
    table = pa.Table.from_pylist([row], schema=SCHEMA)
    save_raw_parquet(table, node_id)


