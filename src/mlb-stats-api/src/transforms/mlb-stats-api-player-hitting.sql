  TRY_CAST("sacFlies" AS BIGINT) AS "sacFlies",
  TRY_CAST("totalBases" AS BIGINT) AS "totalBases",
  TRY_CAST("groundIntoDoublePlay" AS BIGINT) AS "groundIntoDoublePlay",
  TRY_CAST("avg" AS DOUBLE) AS "avg",
  TRY_CAST("obp" AS DOUBLE) AS "obp",
  TRY_CAST("slg" AS DOUBLE) AS "slg",
