SELECT
  CAST(season AS INTEGER) AS season,
  CAST(player_id AS BIGINT) AS player_id,
  CAST(player_name AS VARCHAR) AS player_name,
  CAST(team_id AS BIGINT) AS team_id,
  CAST(team_name AS VARCHAR) AS team_name,
  CAST(league_id AS BIGINT) AS league_id,
  TRY_CAST("gamesPlayed" AS BIGINT) AS "gamesPlayed",
  TRY_CAST("gamesStarted" AS BIGINT) AS "gamesStarted",
  TRY_CAST("gamesPitched" AS BIGINT) AS "gamesPitched",
  TRY_CAST("wins" AS BIGINT) AS "wins",
  TRY_CAST("losses" AS BIGINT) AS "losses",
  TRY_CAST("saves" AS BIGINT) AS "saves",
  TRY_CAST("saveOpportunities" AS BIGINT) AS "saveOpportunities",
  TRY_CAST("holds" AS BIGINT) AS "holds",
  TRY_CAST("blownSaves" AS BIGINT) AS "blownSaves",
  TRY_CAST("hits" AS BIGINT) AS "hits",
  TRY_CAST("runs" AS BIGINT) AS "runs",
  TRY_CAST("earnedRuns" AS BIGINT) AS "earnedRuns",
  TRY_CAST("homeRuns" AS BIGINT) AS "homeRuns",
  TRY_CAST("baseOnBalls" AS BIGINT) AS "baseOnBalls",
  TRY_CAST("intentionalWalks" AS BIGINT) AS "intentionalWalks",
  TRY_CAST("strikeOuts" AS BIGINT) AS "strikeOuts",
  TRY_CAST("hitBatsmen" AS BIGINT) AS "hitBatsmen",
  TRY_CAST("wildPitches" AS BIGINT) AS "wildPitches",
  TRY_CAST("balks" AS BIGINT) AS "balks",
  TRY_CAST("battersFaced" AS BIGINT) AS "battersFaced",
  TRY_CAST("completeGames" AS BIGINT) AS "completeGames",
  TRY_CAST("shutouts" AS BIGINT) AS "shutouts",
  TRY_CAST("era" AS DOUBLE) AS "era",
  TRY_CAST("whip" AS DOUBLE) AS "whip",
  TRY_CAST("strikeoutsPer9Inn" AS DOUBLE) AS "strikeoutsPer9Inn",
  TRY_CAST("walksPer9Inn" AS DOUBLE) AS "walksPer9Inn",
  CAST("inningsPitched" AS VARCHAR) AS "inningsPitched"
FROM "mlb-stats-api-player-pitching"
WHERE season IS NOT NULL
