SELECT
    CAST("FREQ" AS VARCHAR) AS "FREQ",
    CAST("GEOGRAPHY" AS VARCHAR) AS "GEOGRAPHY",
    CAST("CL_ECON_CATEGORIES" AS VARCHAR) AS "CL_ECON_CATEGORIES",
    CAST("GHG_ECCC" AS VARCHAR) AS "GHG_ECCC",
    CAST("TIME_PERIOD" AS VARCHAR) AS "TIME_PERIOD",
    TRY_CAST("OBS_VALUE" AS DOUBLE) AS "OBS_VALUE",
    CAST("UNIT_MEASURE" AS VARCHAR) AS "UNIT_MEASURE",
    CAST("OBS_STATUS" AS VARCHAR) AS "OBS_STATUS",
    CAST("CONF_STATUS" AS VARCHAR) AS "CONF_STATUS"
FROM "canadian-centre-for-energy-information-ccei:ghg-econ-table"
WHERE OBS_VALUE IS NOT NULL
  AND TRY_CAST(OBS_VALUE AS DOUBLE) IS NOT NULL
