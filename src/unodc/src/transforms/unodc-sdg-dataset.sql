    goal,
    target,
    indicator,
    series,
    region,
    subregion,
    intermregion,
    geo,
    iso3_code,
    CAST(TRY_CAST(year AS DOUBLE) AS INTEGER) AS year,
    sex,
    age,
    drug,
    crime,
    dim_comp1,
    value                                     AS value_raw,
