    TRY_CAST(REPLACE(lower_bound, ',', '') AS DOUBLE) AS lower_bound,
    TRY_CAST(REPLACE(upper_bound, ',', '') AS DOUBLE) AS upper_bound,
    obs_status,
    note,
    source,
    time_period,
    altseries
FROM "unodc-sdg-dataset"
WHERE value IS NOT NULL
