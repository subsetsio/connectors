DATA_URL = "https://rplumber.ilo.org/data/indicator"

# Fixed superset schema. Every indicator file maps onto these columns; columns
# the file lacks are written null. A stable, explicit schema is the contract
# that makes parquet safe for all 1416 independent assets.
RAW_SCHEMA = pa.schema([
    ("ref_area", pa.string()),
    ("source", pa.string()),
    ("indicator", pa.string()),
    ("sex", pa.string()),
    ("classif1", pa.string()),
    ("classif2", pa.string()),
    ("time_period", pa.string()),
    ("obs_value", pa.float64()),
    ("obs_status", pa.string()),
])


def _entity_from_node(node_id: str) -> str:
    """ilo-une-tune-sex-age-nb-a -> UNE_TUNE_SEX_AGE_NB_A (inverse of the id rule)."""
    return node_id.removeprefix("ilo-").upper().replace("-", "_")


def _to_float(v):
    if v is None or v == "":
        return None
    try:
        return float(v)
    except ValueError:
        return None


@transient_retry()
def _fetch_csv_gz(entity_id: str) -> bytes:
    resp = get(
        DATA_URL,
        params={"id": entity_id, "format": ".csv.gz"},
        timeout=(10.0, 180.0),
    )
    resp.raise_for_status()
    return gzip.decompress(resp.content)


def fetch_one(node_id: str) -> None:
    asset = node_id  # the runtime passes the spec id; it IS the asset name
    entity_id = _entity_from_node(node_id)
    text = _fetch_csv_gz(entity_id).decode("utf-8-sig")
    reader = csv.DictReader(io.StringIO(text))

    ref_area, source, indicator = [], [], []
    sex, classif1, classif2 = [], [], []
    time_period, obs_value, obs_status = [], [], []
    for row in reader:
        ref_area.append(row.get("ref_area"))
        source.append(row.get("source"))
        indicator.append(row.get("indicator"))
        sex.append(row.get("sex"))
        classif1.append(row.get("classif1"))
        classif2.append(row.get("classif2"))
        time_period.append(row.get("time"))
        obs_value.append(_to_float(row.get("obs_value")))
        obs_status.append(row.get("obs_status"))

    table = pa.table(
        {
            "ref_area": pa.array(ref_area, pa.string()),
            "source": pa.array(source, pa.string()),
            "indicator": pa.array(indicator, pa.string()),
            "sex": pa.array(sex, pa.string()),
            "classif1": pa.array(classif1, pa.string()),
            "classif2": pa.array(classif2, pa.string()),
            "time_period": pa.array(time_period, pa.string()),
            "obs_value": pa.array(obs_value, pa.float64()),
            "obs_status": pa.array(obs_status, pa.string()),
        },
        schema=RAW_SCHEMA,
    )
    save_raw_parquet(table, asset)


