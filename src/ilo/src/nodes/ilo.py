"""ILO (ILOSTAT) connector.

Source: the ILOSTAT bulk download facility, served by the rplumber API at
rplumber.ilo.org. One gzipped tidy-CSV file per indicator-frequency dataset
(the entity union). Mechanism: bulk_csv (per-entity), chosen by research —
https://rplumber.ilo.org/data/indicator?id=<ID>&format=.csv.gz .

Fetch shape: stateless full re-pull. Each indicator file is small (thousands
to a few hundred thousand rows) and the whole corpus re-fetches in minutes, so
we overwrite every run and pick up revisions/late corrections for free — no
watermark, cursor, or maintain branch. (The data endpoint exposes no since/
cursor filter anyway; the TOC's last.update is the only delta signal and that
is the maintain step's concern, not ours.)

Schema handling: every data file is long-format with stable column NAMES but a
varying SET — ref_area, source, indicator, [sex], classif1[, classif2], time,
obs_value, obs_status, plus footnote-code columns. The `sex` dimension gets its
own column whenever SEX is part of the indicator's classification; the other
(at most two) dimensions land in classif1/classif2. The fetch normalizes every
file onto one fixed parquet schema (absent columns -> null), which keeps the raw
layer parquet-safe across all assets. Each per-indicator transform then selects
exactly the dimension columns that indicator actually uses (from ENTITY_META,
derived from the collect catalog's `classification`) and derives a DATE from the
period string per the indicator's frequency (A/Q/M, the id suffix). Footnote
columns are dropped — they are opaque code references without the dictionaries.
"""
import csv
import gzip
