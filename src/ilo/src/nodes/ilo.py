def _date_expr(freq: str) -> str:
    """DuckDB expression turning the period string into the period-start DATE.
    TRY_CAST so a stray malformed period yields a null date rather than failing
    the whole node."""
    if freq == "Q":  # e.g. 2025Q3
        return (
            "make_date(TRY_CAST(substr(time_period, 1, 4) AS INTEGER), "
            "(TRY_CAST(substr(time_period, 6, 1) AS INTEGER) - 1) * 3 + 1, 1)"
        )
    if freq == "M":  # e.g. 2016M04
        return (
            "make_date(TRY_CAST(substr(time_period, 1, 4) AS INTEGER), "
            "TRY_CAST(substr(time_period, 6, 2) AS INTEGER), 1)"
        )
    # Annual (and any fallback): e.g. 2011
    return "make_date(TRY_CAST(substr(time_period, 1, 4) AS INTEGER), 1, 1)"


def _transform_sql(dep_id: str, entity_id: str) -> str:
    has_sex, n_classif = ENTITY_META.get(entity_id, (1, 2))
    freq = entity_id.rsplit("_", 1)[-1]
    dims = []
    if has_sex:
        dims.append("sex")
    if n_classif >= 1:
        dims.append("classif1")
    if n_classif >= 2:
        dims.append("classif2")
    dim_lines = "".join(f"            {d},\n" for d in dims)
    return (
        "\n        SELECT\n"
        f"            {_date_expr(freq)} AS date,\n"
        "            time_period,\n"
        "            ref_area,\n"
        "            source,\n"
        f"{dim_lines}"
        "            obs_value\n"
        f'        FROM "{dep_id}"\n'
        "        WHERE obs_value IS NOT NULL\n"
        "          AND time_period IS NOT NULL\n"
    )


def _transform_key(entity_id: str) -> tuple:
    # The published grain is the full dimensional combination of the ILOSTAT
    # long series: ref_area x source x period, plus exactly the dimension
    # columns this indicator carries (mirrors _transform_sql's dim selection).
    has_sex, n_classif = ENTITY_META.get(entity_id, (1, 2))
    dims = []
    if has_sex:
        dims.append("sex")
    if n_classif >= 1:
        dims.append("classif1")
    if n_classif >= 2:
        dims.append("classif2")
    return ("ref_area", "source", "time_period", *dims)


